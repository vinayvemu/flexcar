// Define a set of real US ZIP codes. With ~100 vehicles and 10 ZIPs,
// each ZIP will naturally have ~10 vehicles.

export const ZIP_CODES: string[] = [
  "10001",
  "30301",
  "60601",
  "73301",
  "80202",
  "94105",
  "98101",
  "02108",
  "15222",
  "85001",
];

// Assign zip codes in a round-robin manner to ensure distribution in hardcoded approach but
// in real time we either should vehicles by zzipcode or each vehicle can have asociated zipcode list
// so that we can use it for search result behaviour
export const VEHICLES: Vehicle[] = VEHICLES_BASE.map((vehicle, index) => ({
  ...vehicle,
  zipCode: ZIP_CODES[index % ZIP_CODES.length],
}));

// Precompute counts per ZIP for convenience
export const ZIP_CODE_TO_COUNT: Record<string, number> = VEHICLES.reduce(
  (acc, v) => {
    acc[v.zipCode] = (acc[v.zipCode] || 0) + 1;
    return acc;
  },
  {} as Record<string, number>
);

